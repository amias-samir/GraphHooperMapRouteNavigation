/// Enum [NavigationProfile] representing different navigation profiles
/// available in the GraphHopper API.
///
/// Each [NavigationProfile] corresponds to a mode of transportation.
enum NavigationProfile {
  /// The [car] profile is used for generating routes
  /// suitable for driving.
  car,

  /// The [foot] profile is used for generating routes
  /// suitable for walking.
  foot,
}

